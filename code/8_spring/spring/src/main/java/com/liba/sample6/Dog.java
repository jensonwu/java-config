package com.liba.sample6;

public class Dog {
    private String name;

    private String age;

    public String toString() {
        return "name = " + this.name + ", age = " + this.age;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(String age) {
        this.age = age;
    }
}
